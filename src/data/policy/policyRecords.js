function source(title, type = 'manifesto', extra = {}) {
  const normalizedType = normalizeSourceType(type)
  return {
    type: normalizedType,
    title,
    url: SOURCE_URLS[title] || '',
    priority: getSourcePriority(normalizedType),
    ...extra,
  }
}

function record({
  id,
  party,
  area,
  topic,
  title,
  stanceScore = 0,
  summary,
  details,
  sources,
  rawClaims = [],
  sourceClaims = [],
  sourcePriority: controllingSourcePriority = null,
  pledgeType = 'policy',
  status = 'active',
  coverage = 'standard',
  confidence = 'high',
}) {
  const claimInput = Array.isArray(rawClaims) && rawClaims.length ? rawClaims : sourceClaims
  const evidence = resolveSourceEvidence({ sources, rawClaims: claimInput })

  return {
    id,
    party,
    area,
    topic,
    title: title || topic || area,
    stanceScore,
    stanceLabel: getStanceLabel(stanceScore),
    pledgeType,
    status,
    summary,
    details,
    rawClaims: evidence.rawClaims,
    sources: evidence.sources,
    controllingSource: evidence.controllingSource,
    sourcePriority: controllingSourcePriority,
    confidence,
    coverage,
    updatedAt: UPDATED_AT,
  }
}

